    private DaemonThread myThread = null;
    int id;
    VideoCapture webSource = null;
    Mat frame = new Mat();
    CascadeClassifier faceDetector = new CascadeClassifier("src\\photo\\haarcascade_frontalface_alt.xml");
    LBPHFaceRecognizer recognizer = LBPHFaceRecognizer.create();
    BytePointer mem = new BytePointer();
    RectVector detectedFaces = new RectVector();
    NhanVien nv = new NhanVien();
    NhanVienDAO dao = new NhanVienDAO();

    class DaemonThread implements Runnable {

        protected volatile boolean runnable = false;

        @Override
        public void run() {
            synchronized (this) {
                while (runnable) {
                    if (webSource.grab()) {
                        try {
                            webSource.retrieve(frame);
                            Graphics g = panelCamera.getGraphics();
                            Mat imageColor = new Mat();
                            imageColor = frame;
                            Mat imageGray = new Mat();
                            cvtColor(imageColor, imageGray, COLOR_BGRA2GRAY);
                            faceDetector.detectMultiScale(imageGray, detectedFaces, 1.1, 1, 1, new Size(200, 200), new Size(500, 500));
                            for (int i = 0; i < detectedFaces.size(); i++) {
                                Rect dadosFace = detectedFaces.get(i);
                                rectangle(frame, dadosFace, new Scalar(0, 255, 0, 0));
                                Mat faceCapturada = new Mat(imageGray, dadosFace);
                                opencv_imgproc.resize(faceCapturada, faceCapturada, new Size(160, 160));

                                IntPointer rotulo = new IntPointer(1);
                                DoublePointer confidence = new DoublePointer(1);
                                recognizer.predict(faceCapturada, rotulo, confidence);
                                int prediction = rotulo.get(0);
                                if (prediction == -1) {
                                    rectangle(frame, dadosFace, new Scalar(0, 255, 0, 0));
                                    id = 0;
                                    JOptionPane.showMessageDialog(null, "Không có dữ liệu");
                                    check.dispose();
                                    myThread.runnable = false;
                                    webSource.release();
                                    try {
                                        new Login().setVisible(true);
                                    } catch (Exception e) {
                                        e.printStackTrace();
                                    }
                                } else {
                                    rectangle(frame, dadosFace, new Scalar(0, 255, 0, 0));
                                    id = prediction;
                                    lblId.setText("NV" + id);
                                    rec();
                                }
                            }

                            imencode(".bmp", frame, mem);
                            Image im = ImageIO.read(new ByteArrayInputStream(mem.getStringBytes()));
                            BufferedImage buff = (BufferedImage) im;
                            if (g.drawImage(buff, 0, 0, 400, 357, 0, 0, buff.getWidth(), buff.getHeight(), null)) {
                                if (runnable == false) {
                                    System.out.println("Paused ..... ");
                                    this.wait();
                                }
                            }
                        } catch (Exception ex) {
                        }
                    }
                }
            }
        }
    }

    private void rec() {

        new Thread() {
            @Override
            public void run() {
                nv = dao.selectById(lblId.getText().trim());
                Login.login.txtUser.setText(nv.getMaNhanVien());
                Login.login.pwdPass.setText(nv.getMatKhau());
                myThread.runnable = false;
                webSource.release();
                check.dispose();
                Login.login.setVisible(true);
            }
        }.start();
    }

